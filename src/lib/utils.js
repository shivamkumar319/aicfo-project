export function tr(lang, key, replacements = {}) {
  const l = T[lang] || T.hh
  let s = l[key] || T.hh[key] || key
  Object.entries(replacements).forEach(([k, v]) => {
    s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), v)
  })
  return s
}

export function fmt(n) {
  const a = Math.abs(n), s = n < 0 ? '-' : ''
  if (a >= 100000) return s + '₹' + (a / 100000).toFixed(1) + 'L'
  if (a >= 1000) return s + '₹' + Math.round(a / 1000) + 'K'
  return s + '₹' + Math.round(a)
}

export function fdate(d, lang) {
  if (!d) return ''
  return new Date(d).toLocaleDateString(lang === 'hi' ? 'hi-IN' : 'en-IN', { day: 'numeric', month: 'short' })
}

export function daysTo(ds) {
  const t = new Date(); t.setHours(0, 0, 0, 0)
  const d = new Date(ds); d.setHours(0, 0, 0, 0)
  return Math.ceil((d - t) / 86400000)
}

export function calcHealthScore(entries, rel, onTimePct, avgDays) {
  let score = 0
  const otp = onTimePct != null ? parseFloat(onTimePct) : null
  const avg = avgDays != null ? parseFloat(avgDays) : null
  const ovr = entries.filter(e => daysTo(e.date) < 0).length

  if (otp != null) {
    if (otp >= 90) score += 40
    else if (otp >= 75) score += 28
    else if (otp >= 55) score += 15
    else score += 5
  } else score += 20

  if (avg != null) {
    if (avg <= -1) score += 25
    else if (avg <= 3) score += 20
    else if (avg <= 7) score += 10
  } else score += 12

  if (ovr === 0) score += 20
  else if (ovr === 1) score += 10

  if (rel === 'vip') score += 15
  else if (rel === 'regular') score += 12
  else if (rel === 'newc') score += 8

  score = Math.min(Math.max(score, 0), 100)
  const grade = score >= 80 ? 'Excellent' : score >= 60 ? 'Good' : score >= 40 ? 'Fair' : 'Poor'
  const cls = score >= 80 ? 'excellent' : score >= 60 ? 'good' : score >= 40 ? 'fair' : 'poor'
  return { score, grade, cls }
}

